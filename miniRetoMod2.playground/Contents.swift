Mini reto modulo 2
*/

import UIKit

for var i = 0; i <= 100; i++
{
    if i % 5 == 0 {
        print("\(i) Bingo!!!")
    }
    else if i % 2 == 0
    {
        print("\(i) par!!!")
    }
    else if i % 2 != 0
    {
        print("\(i) impar!!!")
    }
    else if 30 <= i && i <= 40
    {
        print("\(i) Viva Swift!!!")
    }
 }
